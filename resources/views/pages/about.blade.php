                <h2>A brief history of the school</h2>
                <p>
                    Not much is known about our founding fathers, but this much is certain that it began with a vision -
                    a vision for children who, irrespective of language, creed or colour would grow up in an institution
                    devoid of racial prejudices, would be able to express themselves fearlessly and be taught by
                    teachers totally committed to the cause of sound, all-round, value based education.<br />

                    St. abc' School was established in 1864. <br />
                    It was inaugurated by the then Most Revd. George Edward Cotton- the Bishop of Calcutta on 25th July
                    1864.<br />
                    Almost from its inception, reports show that these were years of stress and struggle. <br />
                    In the first twenty years of its existence the school faced intensive financial difficulties and had
                    to be closed in December 1904. <br />
                    According to reports, there was an upsurge of sentiment on the part of parents, well wishers and old
                    boys at the closure of the school. <br />
                    In 1907, with financial assistance from the Church Education League, the school was reopened.
                    However, again it ran into difficulties and closed in 1918. <br />
                    Relying on the evidence of a few surviving records, we find that the tenacious spirit of the school
                    made it open its doors again in 1924 under the Rectorship of The Rev. Thomas H. Cashmore, also the
                    Vicar of St. abc' Church. Rev. Cashmore established a renaissance in the life of both church and
                    school. He had a strong feeling for the traditions the school had established in the half century of
                    its existence, particularly in the field of sport, looking back to school's triumphant winning of
                    the Beighton Cup in hockey in 1900.

                    So refreshingly different from the desiccated bureaucratic attitude that had condemned the school to
                    closure, Rev. Cashmore's faith in the vitality of his pupils and his humanism indemnified the morale
                    and reputation of the institution. Sports was greatly encouraged as essential to character
                    development and the feats of his pupils in hockey, football, cricket, boxing, swimming and athletics
                    are remembered with affection. Rev. Cashmore left in 1933 to return to England and was succeeded by
                    Mr. I. M. Valentine who contributed much to the consolidation of his predecessor's work during his
                    tenure from 1933 to 1950.

                    The impact of World War II was felt in the period between 1942 and 1946. Air raid shelters were
                    constructed in the school compound and the school premises requisitioned by the R.A.F. on rent. The
                    boarders were sent off to Christ Church School, Jabalpore, and to Bishop Westcott School, Allahabad.
                    The school itself functioned from two rented premises on Ripon Street and Lower Circular Road. The
                    school moved back to its own premises in July 1947.
                    In 1950 Mr. Valentine resigned as Rector and Mr. F. R. Marshall had a short two-year tenure and was
                    succeeded by Mr. E. C. Chippendale in 1952, who ushered in a period of considerable change. There
                    was an increased emphasis on the teaching of science and streaming into Science and Humanities began
                    in Class IX. In 1960 the school had above six hundred pupils on its rolls, and with the care of the
                    Rector and the Board of Governors, was at last financially sound. A new science block was
                    constructed and named after Mr. Chippendale, on his departure in 1961, after nine years of service.
                    Mr. L. N. Bird took charge as Rector in 1961, the designation of Rector being changed shortly
                    thereafter to Principal.</p>
